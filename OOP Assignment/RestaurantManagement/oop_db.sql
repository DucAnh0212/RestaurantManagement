CREATE TABLE Tables (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    FLOOR INT NOT NULL,
    STATUS ENUM('Trống', 'Có khách') NOT NULL DEFAULT 'Trống'
);
CREATE TABLE MenuItems (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    NAME VARCHAR(255) NOT NULL UNIQUE,
    CATEGORY VARCHAR(255) NOT NULL,
    PRICE INT NOT NULL
);
CREATE TABLE Employees (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    FULLNAME VARCHAR(255) NOT NULL,
    WORKSHIFT VARCHAR(255) NOT NULL
);
CREATE TABLE Orders (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    TABLE_ID INT NOT NULL,
    STATUS ENUM('Đang hoạt động', 'Đã thanh toán', 'Đã hủy') NOT NULL DEFAULT 'Đang hoạt động',
    TOTAL_AMOUNT INT DEFAULT 0,
    CREATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (TABLE_ID) REFERENCES Tables(ID)
);
CREATE TABLE OrderItems(
    ID INT PRIMARY KEY AUTO_INCREMENT,
    ORDER_ID INT NOT NULL,
    MENU_ITEM_ID INT NOT NULL,
    QUANTITY INT NOT NULL,
    PRICE INT NOT NULL,
    FOREIGN KEY (ORDER_ID) REFERENCES Orders(ID),
    FOREIGN KEY (MENU_ITEM_ID) REFERENCES MenuItems(ID)
);
